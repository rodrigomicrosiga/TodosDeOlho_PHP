TX_ACAO_PPA            varchar(8) NOT NULL ,
TX_DESCRICAO_PROGRAMA  varchar(250) NOT NULL ,
TX_SITUACAO_PROGRAMA   varchar(15) NOT NULL ,
CD_ORG_SUPERIOR        int(5)  NOT NULL ,
CD_ORG_CONCEDENTE      int(5) NOT NULL ,
DT_DISPONIBILIZACAO    date ,
ANO_DISPONIBILIZACAO   int(4) NOT NULL ,
MES_DISPONIBILIZACAO   int(2) NOT NULL ,
