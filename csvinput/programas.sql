MES_FIM_VIGENCIA       int(2) NOT NULL ,
CD_MANDATARIA          int(5) NOT NULL ,
NM_MANDATARIA          varchar(40) NOT NULL ,
CD_EXECUTOR            int(5) NOT NULL ,
IN_CRITERIO_SELECAO    char(1)  NOT NULL ,
IN_CHAMA_PUBLICO       char(1) NOT NULL ,
IN_REQUER_BENS_SERV    char(1) NOT NULL ,
IN_REQUER_CRONO_DESEMB char(1) NOT NULL ,
IN_ACEITA_PROP_SEM_CADASTRO char(1) NOT NULL ,
IN_REQUER_PLANO_TRABALHO char(1) NOT NULL ,
IN_EMENDA_PARLAMENTAR  char(1) NOT NULL ,
ID_PROGRAMA_CONVENIO   int(5) NOT NULL ,
QT_PROPOSTA            int(6) NOT NULL ,
QT_CONVENIO            int(6) NOT NULL );

